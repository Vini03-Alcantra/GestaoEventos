package br.com.gestao.eventos.model;

public class Professor {
	private int idProfessor;
	private String nomeProfessor;
	private int matriculaProfessor;
	private String cursoProfessorVinculado;
	private String loginProfessor;
	private String senhaPorfessor;
	private String emailProfessor;
	
	public int getIdProfessor() {
		return idProfessor;
	}
	public void setIdProfessor(int idProfessor) {
		this.idProfessor = idProfessor;
	}
	public String getNomeProfessor() {
		return nomeProfessor;
	}
	public void setNomeProfessor(String nomeProfessor) {
		this.nomeProfessor = nomeProfessor;
	}
	public int getMatriculaProfessor() {
		return matriculaProfessor;
	}
	public void setMatriculaProfessor(int matriculaProfessor) {
		this.matriculaProfessor = matriculaProfessor;
	}
	public String getCursoProfessorVinculado() {
		return cursoProfessorVinculado;
	}
	public void setCursoProfessorVinculado(String cursoProfessorVinculado) {
		this.cursoProfessorVinculado = cursoProfessorVinculado;
	}
	public String getLoginProfessor() {
		return loginProfessor;
	}
	public void setLoginProfessor(String loginProfessor) {
		this.loginProfessor = loginProfessor;
	}
	public String getSenhaPorfessor() {
		return senhaPorfessor;
	}
	public void setSenhaPorfessor(String senhaPorfessor) {
		this.senhaPorfessor = senhaPorfessor;
	}
	public String getEmailProfessor() {
		return emailProfessor;
	}
	public void setEmailProfessor(String emailProfessor) {
		this.emailProfessor = emailProfessor;
	}
	
	
}
